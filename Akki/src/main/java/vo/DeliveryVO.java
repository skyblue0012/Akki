package vo;

public class DeliveryVO {
	
	private int d_idx;
	private long o_no;
	private String d_name;
	private String d_tel;
	private String d_telhome;
	private String d_addr;
	private String d_dm;
	
	public int getD_idx() {
		return d_idx;
	}
	public void setD_idx(int d_idx) {
		this.d_idx = d_idx;
	}
	public long getO_no() {
		return o_no;
	}
	public void setO_no(long o_no) {
		this.o_no = o_no;
	}
	public String getD_name() {
		return d_name;
	}
	public void setD_name(String d_name) {
		this.d_name = d_name;
	}
	public String getD_tel() {
		return d_tel;
	}
	public void setD_tel(String d_tel) {
		this.d_tel = d_tel;
	}
	public String getD_telhome() {
		return d_telhome;
	}
	public void setD_telhome(String d_telhome) {
		this.d_telhome = d_telhome;
	}
	public String getD_addr() {
		return d_addr;
	}
	public void setD_addr(String d_addr) {
		this.d_addr = d_addr;
	}
	public String getD_dm() {
		return d_dm;
	}
	public void setD_dm(String d_dm) {
		this.d_dm = d_dm;
	}
	
}
